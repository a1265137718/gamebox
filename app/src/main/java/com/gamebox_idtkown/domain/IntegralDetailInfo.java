public class IntegralDetailInfo {
    private String id;
    private String action_title;
    private String point;
    private String add_time;
    private String goods_log;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAction_title() {
        return action_title;
    }

    public void setAction_title(String action_title) {
        this.action_title = action_title;
    }

    public String getPoint() {
        return point;
    }

    public void setPoint(String point) {
        this.point = point;
    }

    public String getAdd_time() {
        return add_time;
    }

    public void setAdd_time(String add_time) {
        this.add_time = add_time;
    }

    public String getGoods_log() {
        return goods_log;
    }

    public void setGoods_log(String goods_log) {
        this.goods_log = goods_log;
    }
}
